// Declare constants with `let`.
// A constant can appear anywhere a `var` statement can.

let s = "string"
print(s)            // string

let n = 500000000
print(n)            // 500000000

let r = n / 100000000
print(r)            // 5

//r = 2  Cannot assign to value: 'r' is a 'let' constant

